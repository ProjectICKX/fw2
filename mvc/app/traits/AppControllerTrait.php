namespace ickx\fw2\mvc\app\traits;

/**
 * AppControllerTrait
 *
 * @category	Flywheel2
 * @package		mvc
 * @author		wakaba <wakabadou@gmail.com>
 * @license		http://opensource.org/licenses/MIT The MIT License MIT
 * @varsion		2.0.0
 */
trait AppControllerTrait {
	public function userWarn () {
		$this->templateDir	= 'warn';
		$this->template		= 'user';
		return [];
	}

	public function notFoundError () {
		$this->templateDir	= 'error';
		$this->template		= 'not_found';
		return [];
	}

	public function userError () {
		$this->templateDir	= 'error';
		$this->template		= 'user';

		return [];
	}

	public function systemError () {
		$this->templateDir	= 'error';
		$this->template		= 'system';
		return [];
	}
}
